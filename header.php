	$('#site_wrap').removeAttr('style');
	$(window).scrollTop(t);
};

$('#js-sidemenu-button').click(function() {
	show_sidemenu();
	return false;
});

$('#js-sidemenu-close-button').click(function() {
	hide_sidemenu();
	return false;
});

$(window).on('resize orientationchange', function() {
	if ($('body').hasClass('l-sidemenu-active') && $(window).width() <= 1200) {
		hide_sidemenu();
	}
});

});
