	jQuery(function($) {

var show_sidemenu = function(){
	if ($('body').hasClass('l-sidemenu-active')) return false;
	$('body').toggleClass('l-sidemenu-active');

	var t = $(window).scrollTop();
	if (t <= 300) {
		$('#site_wrap').css({
